public class Task6 {
    public static void main(String[] args) {
        System.out.println("Height of a Cylinder: " + args[0]);
        String firstNum = args[0];
        System.out.println("Radius of a Cylinder: " + args[1]);
        String secondNum = args[1];
        double result = (Math.PI * (Math.pow(Double.parseDouble(secondNum), 2)) * Math.pow(Double.parseDouble(firstNum), 1));
        System.out.println("Cylinder capacity is: " + result);
    }
}
